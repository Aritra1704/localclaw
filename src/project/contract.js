async function pathExists(targetPath) {
  try {
    await fs.access(targetPath);
    return true;
  } catch {
    return false;
  }
}

function mergeGitIgnore(existingContent) {
  const existingLines = new Set(
    existingContent
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean)
  );

  const missingLines = BASELINE_GITIGNORE_LINES.filter((line) => !existingLines.has(line));
  if (missingLines.length === 0) {
    return existingContent.endsWith('\n') ? existingContent : `${existingContent}\n`;
  }

  const normalizedExisting =
    existingContent.trim().length === 0
      ? ''
      : existingContent.endsWith('\n')
        ? existingContent
        : `${existingContent}\n`;

  return `${normalizedExisting}${missingLines.join('\n')}\n`;
}

async function walkWorkspaceForJunk(rootPath, currentPath, matches) {
  const entries = await fs.readdir(currentPath, { withFileTypes: true });

  for (const entry of entries) {
    const absolutePath = path.join(currentPath, entry.name);
    const relativePath = path.relative(rootPath, absolutePath);

    if (isIgnoredSegment(entry.name) || shouldIgnoreWorkspaceEntry(relativePath)) {
      matches.push(absolutePath);
      continue;
    }

    if (entry.isDirectory()) {
      await walkWorkspaceForJunk(rootPath, absolutePath, matches);
    }
  }
}

export function shouldIgnoreWorkspaceEntry(relativePath) {
  return splitSegments(relativePath).some(isIgnoredSegment);
}

export function buildBaselineGitIgnore() {
  return `${BASELINE_GITIGNORE_LINES.join('\n')}\n`;
}

export async function ensureBaselineGitIgnore(workspaceRoot) {
  const gitIgnorePath = path.join(workspaceRoot, '.gitignore');
  const existingContent = (await pathExists(gitIgnorePath))
    ? await fs.readFile(gitIgnorePath, 'utf8')
    : '';
  const mergedContent = mergeGitIgnore(existingContent);

  await fs.mkdir(workspaceRoot, { recursive: true });
  await fs.writeFile(gitIgnorePath, mergedContent, 'utf8');

  return gitIgnorePath;
}

export async function seedRepoContract({ workspaceRoot, task }) {
  const files = getRepoContractFiles(task);
  const artifacts = [];

  await fs.mkdir(workspaceRoot, { recursive: true });
  await ensureBaselineGitIgnore(workspaceRoot);
  artifacts.push({
    artifactType: 'file',
    artifactPath: path.join(workspaceRoot, '.gitignore'),
    metadata: { relativePath: '.gitignore' },
  });

  for (const file of files) {
    const absolutePath = path.join(workspaceRoot, file.relativePath);
    await fs.mkdir(path.dirname(absolutePath), { recursive: true });
    await fs.writeFile(absolutePath, file.content, 'utf8');
    artifacts.push({
      artifactType: 'file',
      artifactPath: absolutePath,
      metadata: { relativePath: file.relativePath },
    });
  }

  await removeWorkspaceJunk(workspaceRoot);

  return {
    summary: `Seeded repo contract kit with ${files.length + 1} files`,
    artifacts,
  };
}

export async function collectWorkspaceJunk(workspaceRoot) {
  if (!(await pathExists(workspaceRoot))) {
    return [];
  }

  const matches = [];
  await walkWorkspaceForJunk(workspaceRoot, workspaceRoot, matches);
  return matches.sort((left, right) => right.length - left.length);
}

export async function removeWorkspaceJunk(workspaceRoot) {
  const junkPaths = await collectWorkspaceJunk(workspaceRoot);

  for (const targetPath of junkPaths) {
    await fs.rm(targetPath, { recursive: true, force: true });
  }

  return junkPaths;
}
