import fs from 'node:fs/promises';
import path from 'node:path';

import { config } from '../config.js';

export const BASELINE_GITIGNORE_LINES = [
  '.DS_Store',
  '._*',
  '.Spotlight-V100',
  '.Trashes',
  '.opskit/settings.local.json',
  'PROJECT_CONTEXT.local.md',
];

const JUNK_SEGMENTS = new Set(['.Spotlight-V100', '.Trashes']);

function normalizePath(value) {
  return value.replace(/\\/g, '/');
}

function splitSegments(value) {
  return normalizePath(value).split('/').filter(Boolean);
}

function isIgnoredSegment(segment) {
  return (
    segment === '.DS_Store' ||
    segment.startsWith('._') ||
    JUNK_SEGMENTS.has(segment)
  );
}

function buildDeployTargetSummary() {
  if (!config.railwayDeployEnabled) {
    return 'Not configured for automatic deploy yet.';
  }

  return [
    `Railway project: ${config.railwayProjectId || 'unset'}`,
    `Railway environment: ${config.railwayEnvironmentId || 'unset'}`,
    `Railway service: ${config.railwayServiceId || 'unset'}`,
  ].join('\n');
}

function buildProjectContext(task) {
  return `# Project Context

## Objective

${task.title}

## Source Task

${task.description}

## Delivery Notes

- Generated by LocalClaw in controlled workspace mode
- Task ID: ${task.id}
- Priority: ${task.priority}
- Source: ${task.source}

## Deployment Target

${buildDeployTargetSummary()}

## Local Overrides

Use \`PROJECT_CONTEXT.local.md\` for machine-specific notes that must not be committed.
`;
}

function buildProjectRules() {
  return `# Project Rules

## Engineering

- Keep changes small, explicit, and readable.
- Prefer deterministic behavior over clever abstractions.
- Do not commit secrets, tokens, or local-only credentials.

## Testing

- Add or update a runnable verification path for meaningful changes.
- Fail fast when required runtime configuration is missing.
- Keep logs useful enough for a human operator to diagnose the latest step.

## Git Hygiene

- Do not commit OS junk such as \`.DS_Store\` or \`._*\`.
- Keep ignored local overrides in \`PROJECT_CONTEXT.local.md\` and \`.opskit/settings.local.json\`.

## Deployment

- Deployment remains approval-gated.
- Treat external actions as blocked until the operator confirms them.
`;
}

function buildOpskitReadme() {
  return `# Opskit

This folder is the committed repo contract for LocalClaw-generated projects.

- \`settings.json\`: committed execution defaults
- \`settings.local.json\`: local-only overrides
- \`commands/\`: reusable task command specs
- \`rules/\`: modular project rules
- \`skills/\`: reusable workflow instructions
- \`agents/\`: role-specific operating notes
`;
}

